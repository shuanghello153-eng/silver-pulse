def sw(t):
    s = set(syn.get(t, [t])) | set(PHRASE.get(t, []))
    return {w for w in s if len(w) >= 2}

boiler = set()
for e in data:
    d = e.get('description') or e.get('desc_cn') or ''
    if PAT in d:
        boiler.add(e.get('name_cn') or e.get('name'))

genuine = []
partial = []
for e in data:
    nm = e.get('name_cn') or e.get('name')
    if nm in boiler:
        continue
    d = e.get('description') or e.get('desc_cn') or ''
    text = nm + ' ' + d
    tags = e.get('tag_l2', [])
    uns = [t for t in tags if sw(t) and not any(w in text for w in sw(t))]
    if tags and len(uns) == len(tags):
        genuine.append((nm, tags, d[:60]))
    elif uns:
        partial.append((nm, tags, uns, d[:50]))

with open('audit_genuine.txt', 'w', encoding='utf-8') as f:
    f.write('=== 真错标候选(剔除44模板企业, 全部标签无支撑) ===\n')
    f.write(f'共 {len(genuine)} 家\n\n')
    for nm, tags, d in genuine:
        f.write(f'{nm:24s} tags={tags} | {d}\n')
    f.write('\n=== 部分有支撑但仍含无支撑项(抽样) ===\n')
    for nm, tags, uns, d in partial[:60]:
        f.write(f'{nm:22s} 无支撑={uns} | tags={tags} | {d}\n')

print('真错标候选:', len(genuine))

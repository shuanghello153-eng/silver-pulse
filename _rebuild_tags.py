    z=zipfile.ZipFile(XLSX); ns='{http://schemas.openxmlformats.org/spreadsheetml/2006/main}'
    ss={}
    t=ET.fromstring(z.read('xl/sharedStrings.xml'))
    for i,n in enumerate(t): ss[i]=''.join(x.text or '' for x in n.iter(ns+'t'))
    rows={}
    for name in z.namelist():
        if re.match(r'xl/worksheets/sheet1\.xml',name):
            x=ET.fromstring(z.read(name))
            for row in x.iter(ns+'row'):
                for c in row.iter(ns+'c'):
                    r=c.get('r'); ty=c.get('t'); v=c.find(ns+'v')
                    if v is None: continue
                    col=re.match(r'([A-Z]+)',r).group(1); rn=int(re.match(r'[A-Z]+(\d+)',r).group(1))
                    val=ss.get(int(v.text),v.text) if ty=='s' else v.text
                    rows.setdefault(rn,{})[col]=val
    out={}
    for rn in range(2,2000):
        if rn in rows:
            nm=(rows[rn].get('B') or '').strip()
            a=(rows[rn].get('C') or '').strip()
            b=(rows[rn].get('D') or '').strip()
            if nm:
                f1=[x.strip() for x in a.split('/') if x.strip()] if a else []
                f2=[x.strip() for x in b.split('/') if x.strip()] if b else []
                out.setdefault(nm,{'f1':[],'f2':[]})
                out[nm]['f1']+=f1; out[nm]['f2']+=f2
    return out

# 合并同类词自带关键词 + 短语词典
def support_words(t):
    sw = set(syn.get(t, [t]))
    sw |= set(PHRASE.get(t, []))
    # 去掉纯泛词(这些不算支撑证据)
    sw = {w for w in sw if len(w) >= 2}
    return sw

out = []
out.append('===== 银脉标签体系自检报告 =====\n')

# ===== L1 结构层 =====
out.append('【结构层 L1】')
# A. 单点标签
single = [c for c, ws in syn.items() if len(ws) == 1 and ws[0] == c]
out.append(f'  单点标签(同类词仅自身, {len(single)}个): {single}')
# B. 跨标签碰撞
w2c = defaultdict(set)
for c, ws in syn.items():
    for w in set(ws):
        w2c[w].add(c)
collide = {w: sorted(cs) for w, cs in w2c.items() if len(cs) > 1}
out.append(f'  跨标签同类词碰撞({len(collide)}处):')
for w, cs in collide.items():
    out.append(f'    "{w}" -> {cs}')
# C. L1==L2
bad = [(e.get('name_cn') or e.get('name'), t) for e in data for t in e.get('tag_l2', []) if t in L1]
out.append(f'  一级==二级同名残留: {len(bad)}')
# D. 幽灵
l2c = Counter()
for e in data:
    for t in e.get('tag_l2', []):
        l2c[t] += 1
ghost = [(t, c) for t, c in l2c.items() if c <= 2]
out.append(f'  幽灵标签(<=2家, {len(ghost)}个): {ghost}')
# E. 标签总数
out.append(f'  二级标签总数: {len(l2c)}  一级标签数: {len(set(t for e in data for t in e.get("tag_l1",[])))}')

# ===== L2 企业层 =====
out.append('\n【企业层 L2】逐家标签-资料一致性')
flag_unsupported = []
flag_all = []
for e in data:
    name = e.get('name_cn') or e.get('name') or ''
    desc = e.get('description') or e.get('desc_cn') or ''
    text = name + ' ' + desc
    tags = e.get('tag_l2', [])
    uns = []
    for t in tags:
        sw = support_words(t)
        if not sw:   # 无支撑词定义, 信任
            continue
        if not any(w in text for w in sw):
            uns.append(t)
    if uns:
        flag_unsupported.append((name, tags, uns, desc[:60]))
    if tags and len(uns) == len(tags):
        flag_all.append((name, tags, desc[:60]))

out.append(f'  挂标签但资料无支撑的企业: {len(flag_unsupported)} 家')
out.append(f'  ★全部标签都无支撑(高度可疑): {len(flag_all)} 家\n')
out.append('--- ★全部无支撑清单(供人工复核) ---')
for name, tags, desc in flag_all:
    out.append(f'  {name:30s} tags={tags} | {desc}')

out.append('\n--- 部分有支撑标签但仍含无支撑项(抽样前40) ---')
for name, tags, uns, desc in flag_unsupported[:40]:
    out.append(f'  {name:28s} 无支撑={uns} | 全tags={tags}')

open('audit_result.txt', 'w', encoding='utf-8').write('\n'.join(out))

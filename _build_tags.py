vague_media=[]
for i,e in enumerate(data):
    name=(e.get('name') or '').strip()
    name_cn=(e.get('name_cn') or '').strip()
    raw_tags=set()
    # a) 系统 tags
    for t in (e.get('tags') or []):
        c=map_raw(t)
        if c: raw_tags.add(c)
    # b) Excel 细分领域_1 (海外权威, 优先)
    ex=[]
    if name_cn in excel: ex=excel[name_cn]
    elif name in excel: ex=excel[name]

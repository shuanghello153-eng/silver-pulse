def apply_updates(data):
    updated = 0
    for e in data:
        if '中国企业图谱' in e.get('source', '') and not e.get('description'):
            result = gen_description(e['name'], e.get('category_l1', ''), e.get('category_l2', ''))
            if isinstance(result, tuple):
                desc, highlight = result
            else:
                desc = result
                highlight = f'{e.get("category_l1", "银发经济")}领域企业'
            
            e['description'] = desc
            if not e.get('highlights'):
                e['highlights'] = highlight
            updated += 1
    
    print(f'Updated: {updated}')
    return data

